  /// Generate barrel file
  Future<void> _generateBarrelFile() async {
    final buffer = StringBuffer();
    
    buffer.writeln('/// Generated models barrel file');
    buffer.writeln('/// DO NOT EDIT - This file is automatically generated');
    buffer.writeln();
    
    buffer.writeln("export 'base_model.dart';");
    buffer.writeln("export 'database_context.dart';");
    
    for (final model in schema.models) {
      buffer.writeln("export '${_toSnakeCase(model.name)}.dart';");
    }

    final file = File('$outputDirectory/models.dart');
    await file.writeAsString(buffer.toString());
  }

  /// Convert string to snake_case
  String _toSnakeCase(String input) {
    return input
        .replaceAllMapped(RegExp(r'[A-Z]'), (match) => '_${match.group(0)?.toLowerCase()}')
        .replaceFirst(RegExp(r'^_'), '');
  }

  /// Convert string to PascalCase
  String _toPascalCase(String input) {
    return input.split('_')
        .map((word) => word.isEmpty ? '' : '${word[0].toUpperCase()}${word.substring(1)}')
        .join('');
  }
