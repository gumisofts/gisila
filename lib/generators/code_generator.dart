/// Main code generator for Gisila ORM
///
/// Coordinates the generation of models, migrations, and other
/// database artifacts from schema definitions.
library gisila.generators.code_generator;

import 'dart:io';
import 'schema_parser.dart';
import 'model_generator.dart';
import 'query_generator.dart';

/// Main code generator class
class CodeGenerator {
  final SchemaDefinition schema;
  final String outputDirectory;

  CodeGenerator({
    required this.schema,
    required this.outputDirectory,
  });

  /// Generate all code artifacts
  Future<void> generateAll() async {
    print('🚀 Starting code generation...');

    // Create base output directory
    final dir = Directory(outputDirectory);
    if (!await dir.exists()) {
      await dir.create(recursive: true);
    }

    // Generate models and queries using part of approach
    await _generateModelsWithPartOf();

    // Generate migrations
    await _generateMigrations();

    // Generate database setup
    await _generateDatabaseSetup();

    print('✅ Code generation completed successfully!');
  }

  /// Generate all models and queries as parts of a single library
  Future<void> _generateModelsWithPartOf() async {
    print('📦 Generating models and queries with part of approach...');

    // Create the parts directory
    final partsDir = '$outputDirectory/generated';
    final dir = Directory(partsDir);
    if (!await dir.exists()) {
      await dir.create(recursive: true);
    }

    // Generate the main library file
    await _generateMainLibraryFile();

    // Generate base model as part
    await _generateBaseModelPart();

    // Generate database context as part
    await _generateDatabaseContextPart();

    final modelGenerator = ModelGenerator(
      schema: schema,
      outputDirectory: partsDir,
    );
    final queryGenerator = QueryGenerator();

    // Generate each model as a part
    for (final model in schema.models) {
      await _generateModelPart(model, modelGenerator, partsDir);
      await _generateQueryPart(model, queryGenerator, partsDir);
    }

    print('   Generated ${schema.models.length} models and queries as parts');
  }

  /// Generate the main library file that includes all parts
  Future<void> _generateMainLibraryFile() async {
    final mainFile = File('$outputDirectory/models.dart');
    final buffer = StringBuffer();

    // Library declaration
    buffer.writeln('/// Generated models and queries library');
    buffer.writeln('/// DO NOT EDIT - This file is automatically generated');
    buffer.writeln();
    buffer.writeln('library gisila.generated.models;');
    buffer.writeln();

    // Common imports
    buffer.writeln("import 'dart:async';");
    buffer.writeln("import 'package:gisila/config/config.dart';");
    buffer
        .writeln("import 'package:gisila/database/postgres/query/query.dart';");
    buffer.writeln(
        "import 'package:gisila/database/postgres/query/operator.dart';");
    buffer.writeln();

    // Part declarations
    buffer.writeln('// Base classes');
    buffer.writeln("part 'generated/base_model.dart';");
    buffer.writeln("part 'generated/database_context.dart';");
    buffer.writeln();

    buffer.writeln('// Generated models');
    for (final model in schema.models) {
      final fileName = _toSnakeCase(model.name);
      buffer.writeln("part 'generated/${fileName}.dart';");
    }
    buffer.writeln();

    buffer.writeln('// Generated query builders');
    for (final model in schema.models) {
      final fileName = _toSnakeCase(model.name);
      buffer.writeln("part 'generated/${fileName}_query.dart';");
    }

    await mainFile.writeAsString(buffer.toString());
  }

  /// Generate base model as a part
  Future<void> _generateBaseModelPart() async {
    final file = File('$outputDirectory/generated/base_model.dart');
    final content = '''part of '../models.dart';

/// Base model class for all generated models
abstract class BaseModel {
  /// Convert model to map for database operations
  Map<String, dynamic> toMap();
  
  /// Validate model instance
  List<String> validate();
}

/// Validation exception for model validation errors
class ValidationException implements Exception {
  final List<String> errors;
  ValidationException(this.errors);

  @override
  String toString() => 'ValidationException: \${errors.join(", ")}';
}
''';

    await file.writeAsString(content);
  }

